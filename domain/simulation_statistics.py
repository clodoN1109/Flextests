class SimulationStats:
    def __init__(
        self,
        duration: float = 0.0,
        max_memory: float = 0.0,
        min_memory: float = 0.0,
        mean_memory: float = 0.0,
    ):
        self.duration = duration
        self.max_memory = max_memory
        self.min_memory = min_memory
        self.mean_memory = mean_memory






